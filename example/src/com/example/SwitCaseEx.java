package com.example;

public class SwitCaseEx {
    public static void main(String[] args) {
        int yas=2;

        switch(yas){
            case 1:
                System.out.println("Bir yasindasin");
                break;
            case 2:
                System.out.println("iki yasindasin");
                break;
            case 3:
                System.out.println("üç yasindasin");
                break;
            default:
                System.out.println("kaç yasindasin?");
        }
    }
}
